##tools for debugging, 5 basics
##traceback - prints out function call stack after an error occurs, nothing if no error
##debug -- flags a function for "debug" mode which allows you to step through the execution of a function one line at a time
##browser-- suspends execution of a function where it is called and puts function in debug mode
##trace -- allows you to insert a debugging code into a function specific place
##recover -- related to traceback allows you to modify the error behavior so that you can browse function call stack
##recover is an error handler function
##print and cat can be nice if not too complex of code, some folks oppose debugging tools
